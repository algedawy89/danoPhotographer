        // Render packages
     window.renderPackages=()=> {
            const container = document.getElementById('packagesContainer');
            container.innerHTML = '';
            
            packages.forEach((package, index) => {
                const packageEl = createPackageElement(package, index);
                container.appendChild(packageEl);
            });
        }
        // Create package element
        function createPackageElement(package, index) {
            const div = document.createElement('div');
            div.className = 'relative glass rounded-2xl overflow-hidden neon-glow card-3d';
            
            const badgeHtml = package.badge ? `
                <div class="absolute top-0 right-0 bg-gradient-to-l from-${package.color} to-secondary text-white px-4 py-2 rounded-bl-2xl text-sm font-bold">
                    <i class="fas fa-crown mr-1"></i>${package.badge}
                </div>
            ` : '';
            
            div.innerHTML = `
                ${badgeHtml}
                <button onclick="togglePackage(${index + 1})" class="w-full p-6 text-right flex justify-between items-center hover:bg-white/10 smooth-transition ${package.badge ? 'pt-12' : ''}">
                    <div>
                        <h3 class="text-xl font-bold text-white">${package.title}</h3>
                        <p class="text-white/80">${package.description}</p>
                    </div>
                    <div class="flex items-center gap-4">
                        <span class="bg-${package.color} text-white px-4 py-2 rounded-full font-bold">${package.price}</span>
                        <i class="fas fa-chevron-down text-white toggle-icon-${index + 1} smooth-transition"></i>
                    </div>
                </button>
                <div class="toggle-content px-6 pb-6" id="package-${index + 1}">
                    <div class="space-y-3">
                        ${package.items.map(item => `
                            <div class="flex items-center gap-3 text-white/90">
                                <i class="fas fa-${item.icon} text-${item.color}"></i>
                                <span>${item.text}</span>
                            </div>
                        `).join('')}
                    </div>
                </div>
            `;
            
            return div;
        }


        // Toggle packages
        function togglePackage(packageNum) {
            const content = document.getElementById(`package-${packageNum}`);
            const icon = document.querySelector(`.toggle-icon-${packageNum}`);
            
            if (content.classList.contains('active')) {
                content.classList.remove('active');
                icon.style.transform = 'rotate(0deg)';
            } else {
                // Close all other packages
                for (let i = 1; i <= packages.length; i++) {
                    if (i !== packageNum) {
                        const otherContent = document.getElementById(`package-${i}`);
                        const otherIcon = document.querySelector(`.toggle-icon-${i}`);
                        if (otherContent) otherContent.classList.remove('active');
                        if (otherIcon) otherIcon.style.transform = 'rotate(0deg)';
                    }
                }
                
                content.classList.add('active');
                icon.style.transform = 'rotate(180deg)';
            }
        }


